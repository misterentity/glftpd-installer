    def export_offline_installer(self):
        if self.testing:
            return
        temp_dir = None
        try:
            save_dir = filedialog.askdirectory(
                title="Select Directory for Offline Installer")
            if not save_dir:
                return

            local_path = os.path.dirname(os.path.abspath(__file__))
            install_script = os.path.join(local_path, "install.sh")

            if not os.path.exists(install_script):
                if not self.ssh_client:
                    messagebox.showerror(
                        "Error",
                        "install.sh not found locally. "
                        "Connect via SSH to download it.")
                    return

                self.log_message("Downloading install.sh from server...")
                temp_dir = tempfile.mkdtemp()
                try:
                    self.ssh_client.exec_command(
                        "mkdir -p /tmp/glftpd-installer")
                    install_script = os.path.join(temp_dir, "install.sh")
                    self.sftp_client.get(
                        "/tmp/glftpd-installer/install.sh", install_script)
                except Exception as e:
                    shutil.rmtree(temp_dir, ignore_errors=True)
                    messagebox.showerror(
                        "Error", f"Failed to download install.sh: {e}")
                    return

            zip_path = os.path.join(save_dir,
                                    "glftpd-offline-installer.zip")
            with zipfile.ZipFile(zip_path, "w",
                                 zipfile.ZIP_DEFLATED) as zf:
                zf.write(install_script, "install.sh")

                with tempfile.NamedTemporaryFile(
                        mode="w", suffix=".cache", delete=False) as tmp:
                    tmp.write(self.generate_cache_content())
                    cache_tmp = tmp.name
                try:
                    zf.write(cache_tmp, "install.cache")
                finally:
                    os.unlink(cache_tmp)

                zf.writestr("README.txt",
                    "GLFTPD OFFLINE INSTALLER\n"
                    "============================\n\n"
                    "This package contains everything needed for an "
                    "unattended installation of glFTPd.\n\n"
                    "Instructions:\n"
                    "1. Extract the contents of this zip file "
                    "to the target server\n"
                    "2. Make install.sh executable: "
                    "chmod +x install.sh\n"
                    "3. Run the installation as root: "
                    "sudo ./install.sh\n\n"
                    "The installation will proceed unattended using "
                    "the configuration in install.cache.\n")

            if temp_dir:
                shutil.rmtree(temp_dir, ignore_errors=True)

            self.log_message(
                f"Offline installer exported to {zip_path}", "success")
        except Exception as e:
            if temp_dir:
                shutil.rmtree(temp_dir, ignore_errors=True)
            self.log_message(
                f"Error exporting offline installer: {e}", "error")
            messagebox.showerror(
                "Error", f"Failed to export offline installer: {e}")

    # -- Cache generation -------------------------------------------------------

    def generate_cache_content(self):
        lines = []
        for entry in CACHE_SCHEMA:
            if len(entry) == 1 and entry[0] == _SECTIONS_MARKER:
                for i, sec in enumerate(self.section_entries, 1):
                    lines.append(
                        f'section{i}="{sec["name"].get()}" '
                        f'# Name of section {i}')
                    lines.append(
                        f'section{i}dated="{sec["dated"].get()}" '
                        f'# Dated section?')
                for i, sec in enumerate(self.section_entries, 1):
                    lines.append(
                        f'sectionpath{i}="{sec["path"].get()}" '
                        f'# Path for section {i}')
            elif len(entry) == 1:
                lines.append(entry[0])
            else:
                key, attr, comment = entry
                val = getattr(self, attr).get()
                lines.append(f'{key}="{val}" # {comment}')
        return "\n".join(lines) + "\n"


