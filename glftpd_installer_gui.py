    def _parse_cache(self, content):
        kv_re = re.compile(r'^(\w+)="((?:[^"\\]|\\.)*)"', re.MULTILINE)
        data = dict(kv_re.findall(content))

        cache_to_attr = {}
        for entry in CACHE_SCHEMA:
            if len(entry) == 3:
                cache_key, attr, _ = entry
                cache_to_attr[cache_key] = attr

        self._suppress_rebuild = True
        try:
            for cache_key, value in data.items():
                if cache_key in cache_to_attr:
                    getattr(self, cache_to_attr[cache_key]).set(value)

            sec_count = int(data.get("sections", "3"))
            self.sections.set(str(sec_count))

            while len(self.section_entries) < sec_count:
                self.section_entries.append({
                    "name": self._make_var(""),
                    "path": self._make_var("/site/"),
                    "dated": self._make_var("n"),
                })
            self.section_entries = self.section_entries[:sec_count]

            sec_name_re = re.compile(r"^section(\d+)$")
            sec_dated_re = re.compile(r"^section(\d+)dated$")
            sec_path_re = re.compile(r"^sectionpath(\d+)$")

            for key, value in data.items():
                for pattern, field_key in [
                    (sec_name_re, "name"),
                    (sec_dated_re, "dated"),
                    (sec_path_re, "path"),
                ]:
                    m = pattern.match(key)
                    if m:
                        idx = int(m.group(1)) - 1
                        if 0 <= idx < len(self.section_entries):
                            self.section_entries[idx][field_key].set(value)
                        break
        finally:
            self._suppress_rebuild = False

        self._rebuild_sections()

    # -- Installation -----------------------------------------------------------

    def start_installation(self):
        if self.testing:
            return
        if not self.ssh_client:
            messagebox.showerror("Error",
                                 "Please establish SSH connection first!")
            return
        if self.installation_running:
            return
        if not self.sitename.get().strip():
            messagebox.showerror("Error", "Please enter a site name")
            return

        host = self.ssh_host.get()
        if not messagebox.askyesno(
                "Confirm Installation",
                f"Install glFTPd on {host}?\n\n"
                "This will modify the remote server."):
            return

        self.installation_running = True
        self.install_button["state"] = "disabled"
        self.progress_bar["value"] = 0
        self.current_step = 0

        self.notebook.select(self.log_frame)

        thread = threading.Thread(target=self._run_installation, daemon=True)
        thread.start()

    def _check_ssh_alive(self):
        transport = self.ssh_client.get_transport() if self.ssh_client else None
        if not transport or not transport.is_active():
            raise ConnectionError("SSH connection lost")

    def _exec_checked(self, cmd, timeout=30):
        self._check_ssh_alive()
        stdin, stdout, stderr = self.ssh_client.exec_command(
            cmd, timeout=timeout)
        exit_code = stdout.channel.recv_exit_status()
        out = stdout.read().decode(errors="replace")
        err = stderr.read().decode(errors="replace")
        return exit_code, out, err

    def _run_installation(self):
        remote_path = "/tmp/glftpd-installer"
        install_ok = False
        try:
            self.update_progress(1, "CREATING DIR")
            code, _, err = self._exec_checked(f"mkdir -p {remote_path}")
            if code != 0:
                raise RuntimeError(
                    f"Failed to create remote directory: {err}")
            self.log_message("Created installation directory")

            self.update_progress(2, "TRANSFERRING")
            self._check_ssh_alive()
            local_path = os.path.dirname(os.path.abspath(__file__))
            self.sftp_client.put(
                os.path.join(local_path, "install.sh"),
                f"{remote_path}/install.sh")
            self.log_message("Transferred installation script")

            cache_content = self.generate_cache_content()
            with tempfile.NamedTemporaryFile(
                    mode="w", suffix=".cache", delete=False) as tmp:
                tmp.write(cache_content)
                tmp_path = tmp.name
            try:
                self._check_ssh_alive()
                self.sftp_client.put(tmp_path,
                                     f"{remote_path}/install.cache")
            finally:
                os.unlink(tmp_path)
            self.log_message("Transferred configuration file")

            self.update_progress(3, "SETTING UP")
            code, _, err = self._exec_checked(
                f"chmod +x {remote_path}/install.sh")
            if code != 0:
                raise RuntimeError(f"Failed to chmod install.sh: {err}")
            self.log_message("Made installation script executable")

            self.update_progress(4, "INSTALLING")
            self._check_ssh_alive()
            stdin, stdout, stderr = self.ssh_client.exec_command(
                f"sudo -S {remote_path}/install.sh")
            stdin.write(self.ssh_password.get() + "\n")
            stdin.flush()

            channel = stdout.channel
            channel.settimeout(600)
            buf_out = b""
            buf_err = b""
            while not channel.exit_status_ready():
                if channel.recv_ready():
                    chunk = channel.recv(4096)
                    buf_out += chunk
                    while b"\n" in buf_out:
                        line, buf_out = buf_out.split(b"\n", 1)
                        self.log_message(
                            line.decode(errors="replace").rstrip())
                if channel.recv_stderr_ready():
                    buf_err += channel.recv_stderr(4096)
                time.sleep(0.05)

            while channel.recv_ready():
                buf_out += channel.recv(4096)
            while channel.recv_stderr_ready():
                buf_err += channel.recv_stderr(4096)

            if buf_out:
                for line in buf_out.decode(errors="replace").splitlines():
                    self.log_message(line.rstrip())

            exit_code = channel.recv_exit_status()
            errors = buf_err.decode(errors="replace")
            if errors:
                self.log_message(f"Errors:\n{errors}", "error")

            if exit_code != 0:
                raise RuntimeError(
                    f"install.sh exited with code {exit_code}")

            install_ok = True
            self.update_progress(5, "CLEANING UP")
            self._exec_checked(f"rm -rf {remote_path}")
            self.log_message("Installation completed successfully!",
                             "success")
            self.root.after(0, messagebox.showinfo,
                            "Success", "Installation completed!")

        except Exception as e:
            self.log_message(f"Error during installation: {e}", "error")
            if not install_ok:
                self.log_message(
                    "Remote files kept for debugging at "
                    f"{remote_path}", "warning")
            self.root.after(0, messagebox.showerror,
                            "Error", f"Installation failed: {e}")
        finally:
            self.installation_running = False
            self.root.after(0, self._reset_install_button)

