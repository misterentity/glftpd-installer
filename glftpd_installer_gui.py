class _SimpleVar:
    """Lightweight StringVar substitute for headless/testing mode."""

    def __init__(self, value=""):
        self._value = value
        self._trace_cbs = []

    def get(self):
        return self._value

    def set(self, value):
        self._value = value
        for cb in self._trace_cbs:
            cb(None, None, "write")

    def trace_add(self, mode, callback):
        if mode == "write":
            self._trace_cbs.append(callback)
        return str(id(callback))

    def trace_remove(self, mode, cbname):
        self._trace_cbs = [
            cb for cb in self._trace_cbs if str(id(cb)) != cbname]


class _ToolTip:
    """Lightweight hover tooltip for any widget."""

    def __init__(self, widget, text):
        self.widget = widget
        self.text = text
        self.tip_window = None
        widget.bind("<Enter>", self._show)
        widget.bind("<Leave>", self._hide)

    def _show(self, event=None):
        if self.tip_window:
            return
        x = self.widget.winfo_rootx() + 20
        y = self.widget.winfo_rooty() + self.widget.winfo_height() + 5
        self.tip_window = tw = tk.Toplevel(self.widget)
        tw.wm_overrideredirect(True)
        tw.wm_geometry(f"+{x}+{y}")
        tk.Label(tw, text=self.text, justify=tk.LEFT,
                 background="#1A1A2E", foreground="#E0E0E0",
                 relief=tk.SOLID, borderwidth=1,
                 font=("Consolas", 9), wraplength=300,
                 padx=6, pady=4).pack()

    def _hide(self, event=None):
        if self.tip_window:
            self.tip_window.destroy()
            self.tip_window = None


