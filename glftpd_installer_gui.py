    # -- SSH connection ---------------------------------------------------------

    def connect_ssh(self):
        host = self.ssh_host.get().strip()
        if not host:
            messagebox.showerror("Error", "Please enter a hostname")
            return

        try:
            port = int(self.ssh_port.get())
        except ValueError:
            messagebox.showerror("Error", "SSH port must be a number")
            return

        username = self.ssh_username.get().strip()
        if not username:
            messagebox.showerror("Error", "Please enter a username")
            return

        self.disconnect_ssh()

        try:
            self.ssh_client = paramiko.SSHClient()
            self.ssh_client.load_system_host_keys()
            self.ssh_client.set_missing_host_key_policy(
                paramiko.WarningPolicy())

            connect_kw = {
                "hostname": host, "port": port,
                "username": username, "timeout": 10,
            }
            keyfile = self.ssh_keyfile.get().strip()
            if keyfile and os.path.isfile(keyfile):
                connect_kw["key_filename"] = keyfile
            if self.ssh_password.get():
                connect_kw["password"] = self.ssh_password.get()

            self.ssh_client.connect(**connect_kw)
            self.sftp_client = self.ssh_client.open_sftp()
            self.log_message("SSH connection established successfully!",
                             "success")
        except Exception as e:
            self.ssh_client = None
            self.log_message(f"Failed to connect: {e}", "error")
            messagebox.showerror("Error", f"Failed to connect: {e}")

        self._update_status()

    def disconnect_ssh(self):
        for client in (self.sftp_client, self.ssh_client):
            if client:
                try:
                    client.close()
                except Exception:
                    pass
        self.sftp_client = None
        self.ssh_client = None
        self._update_status()

    def _on_close(self):
        self.disconnect_ssh()
        self.root.destroy()

